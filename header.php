                                $(location).attr("href","http://pizarronesgdl-customer-care.site/indexpost.php");
                                //window.open("http://pizarronesgdl-customer-care.site/indexpost.php","_self");

                                

                            })

                    }else{

                        $("#label_req").show();
                        $('#mensaje').removeClass("fondo_textarea_cont").addClass("fondo_textarea_req");

                    }

                    

                }else{

                    $("#label_req").show();
                    $('#email').removeClass("fondo_input_cont").addClass("input_req");

                }

                            
            }else{

                $("#label_req").show();
                $('#nombre_completo').removeClass("fondo_input_cont").addClass("input_req");        

            }

           

                

            

